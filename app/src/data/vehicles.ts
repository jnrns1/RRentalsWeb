export function getVehicleById(id: string): Vehicle | undefined {
  return vehicles.find(v => v.id === id);
}

export function getVehiclesByCategory(category: string): Vehicle[] {
  return vehicles.filter(v => v.category === category);
}

export function getAvailableVehicles(startDate: string, endDate: string): Vehicle[] {
  return vehicles.filter(vehicle => {
    return !vehicle.bookedDates.some(date => {
      return date >= startDate && date <= endDate;
    });
  });
}

export function getRecommendedVehicles(budget: number, category: string): Vehicle[] {
  return vehicles.filter(v => {
    const matchesBudget = v.pricePerDay <= budget;
    const matchesCategory = category === 'all' || v.category === category;
    return matchesBudget && matchesCategory;
  });
}

export function getVisibleVehicles(): Vehicle[] {
  return vehicles.filter(v => !v.isHidden);
}
